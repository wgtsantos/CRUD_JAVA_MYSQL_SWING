    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String nome = jTextField1.getText();
        String email = jTextField2.getText();
        String cpf = jTextField3.getText();
        String telefone = jTextField4.getText();
        String senha = jPasswordField1.getText();
        String confirma_senha = jPasswordField2.getText();
        
        if (nome.equals("") || email.equals("") || 
            cpf.equals("") || telefone.equals("") ||
            senha.equals("") || confirma_senha.equals("")) {
            
          JOptionPane.showMessageDialog(null,
                    "Por Favor!! Preencha todos os campos!", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
        
        else if (!senha.equals(confirma_senha)) {
            
          JOptionPane.showMessageDialog(null,
                    "Senhas n√£o conferem!!", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            
            Usuario user = new Usuario();
            
            user.setNome(nome);
            user.setEmail(email);
            user.setCpf(cpf);
            user.setTelefone(telefone);
            user.setSenha(senha);
            
            UserController usercontrol = new UserController();
            
            usercontrol.Salvar(user);
            
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jPasswordField1.setText("");
            jPasswordField2.setText("");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

